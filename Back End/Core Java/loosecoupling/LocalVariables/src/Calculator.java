
public class Calculator 
{
	public int add(int a)
	{
		return a+a;
	}
	public int add(int a,int b)
	{
		return a+b;
	}
	public double add(double a)
	{
		return a+a;
	}
	public static void mul(int a)
	{
		System.out.println("multiplication1="+(a*a));
	}
	public static void mul(int a,int b)
	{
		System.out.println("multiplication2="+(a*b));
	}
	public static void mul(double a)
	{
		System.out.println("multiplication3="+(a*a));
	}

}
